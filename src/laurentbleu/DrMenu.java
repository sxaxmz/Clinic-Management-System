    public void Update_Table(){
        try {
           String strDate;
            Calendar cal = Calendar.getInstance();
            int month = cal.get(Calendar.MONTH)+1;
            int year = cal.get(Calendar.YEAR);
            int day = cal.get(Calendar.DAY_OF_MONTH);
            strDate = year +"-"+ month +"-"+ day;          
            System.out.println(strDate);
            
            
            String sql = "select ap_id as 'Appointment ID',p_id as 'Patient ID',p_name as 'Patient Name', ap_service as 'Service' from appointments where dr_id=? and ap_date =?";
            pst=conn.prepareStatement(sql);
            pst.setString(1, dr_id);
            pst.setString(2, strDate);
            rs=pst.executeQuery();
            appointment.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex);
            System.out.println(ex);
        }finally {
            try {
                
                rs.close();
                pst.close();
                //conn.close();
            } catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        }
    }
    
